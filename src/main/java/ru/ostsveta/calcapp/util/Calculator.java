package ru.ostsveta.calcapp.util;

public class Calculator {

    public static int add(int x, int y) {
        int res = x + y;
        return res;
    }

    public static int subtrakt(int x, int y) {
        int res = x - y;
        return res;
    }

    public static int multiply(int x, int y) {
        int res = x * y;
        return res;
    }
}

